export class TextBlock {
  public text:   string
  public data:   number[]
  public width:  number
  public height: number
  constructor(text: string) {
    this.text    = text.toUpperCase()
    this.width   = (this.text.length * 6)
    this.height  = 5
    this.data    = new Array(this.width * this.height)
    let offset_x = 0
    for (let i = 0; i < this.text.length; i++) {
      const tab = character_map[this.text.charAt(i)] as number[]
      for(let iy = 0; iy < 5; iy ++) {
        for(let ix = 0; ix < 5; ix ++) {
          this.data[this.offset(offset_x + ix, iy, this.width)] = tab[this.offset(ix, iy, 5)]
        }
      }
      offset_x += 6
    }
  }
  /**
   * returns the pixel value at the given position.
   * @param {number} x the x position.
   * @param {number} y the y position.
   * @returns {number}
   */
  public get(x: number, y: number): number {
    return this.data[this.offset(x, y, this.width)]
  }

  private offset(x: number, y: number, width: number): number {
    return x + (y * width)
  }
}